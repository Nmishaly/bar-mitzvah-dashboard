// Get sorted parasha list for a given Hebrew year
function getParashiyotForYear(hebrewYear) {
    const isLeap = isHebrewLeapYear(hebrewYear);
    return isLeap ? [...ALL_PARSHIYOT_LEAP] : [...PARSHIYOT_REGULAR];
}

// Get all available parashiyot across all years (for global dropdown)
function getAllParashiyot() {
    const set = new Set();
    Object.values(PARASHA_DATES).forEach(yearData => {
        Object.keys(yearData).forEach(p => set.add(p));
    });
    // Return in canonical order (union of all names)
    const allNames = [...new Set([...ALL_PARSHIYOT_LEAP, ...PARSHIYOT_REGULAR])];
    return allNames.filter(p => set.has(p));
}

// Look up Shabbat date for a parasha in a given Hebrew year
function getParashaDate(parashaName, hebrewYear) {
    const yearData = PARASHA_DATES[hebrewYear];
    if (!yearData) return null;
    return yearData[parashaName] || null;
}

// Given a Gregorian event date, find which Hebrew year it belongs to for parasha purposes
function getHebrewYearForDate(dateStr) {
    const d = new Date(dateStr);
    return getHebrewYear(d);
}

// Find the parasha name for a given date (reverse lookup)
function getParashaByDate(dateStr) {
    for (const [year, parashiyot] of Object.entries(PARASHA_DATES)) {
        for (const [parasha, date] of Object.entries(parashiyot)) {
            if (date === dateStr) return { parasha, year: parseInt(year) };
        }
    }
    return null;
}

// Format date as DD/MM/YYYY Hebrew style
function formatHebrewDate(dateStr) {
    if (!dateStr) return '';
    const d = new Date(dateStr + 'T12:00:00');
    return d.toLocaleDateString('he-IL', { day: 'numeric', month: 'long', year: 'numeric' });
}
